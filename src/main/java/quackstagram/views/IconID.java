package quackstagram.views;

/**
 * The {@code IconID} enum defines identifiers for icons used in the application's UI.
 * Each enum constant represents an icon that corresponds to a specific feature or navigation element.
 */
public enum IconID {
    ADD,     // Icon for adding new content, typically represents a post or image upload feature
    HEART,   // Icon used to represent likes or favorites
    HOME,    // Icon representing the home or main feed screen
    PROFILE, // Icon for user profile access or settings
    SEARCH   // Icon used for search functionality, indicating a feature to find specific content or users
}
