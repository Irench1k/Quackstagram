package quackstagram.views;

/**
 * The {@code ColorID} enum defines identifiers for different color themes used in the UI components of the application.
 * Each enum constant represents a specific type of UI element or state, allowing for a centralized theme management.
 */
public enum ColorID {
    BACKGROUND_HEADER,          // Color for the main header background
    BACKGROUND_HEADER_SECONDARY,// Color for secondary headers or sub-headers
    MAIN_BACKGROUND,            // Main background color for the application
    MINOR_BACKGROUND,           // Background color for minor elements or secondary panels
    LIKE_BUTTON,                // Color for the like button or similar interactive elements
    PRIMARY_lOGIN_BUTTON,       // Color for the primary button used in login/signup forms
    BACKGROUND_SPACING,         // Color for spacing or separators between UI elements
    FOLLOW_BUTTON,              // Color for the follow button or similar interactive elements
    TEXT_PRIMARY,               // Primary text color
    TEXT_SECONDARY,             // Secondary text color for less emphasized text
    ENTER_COMPONENT,            // Background color for input components like text fields
    OPPOSITE_TEXT               // Text color that contrasts with the main background, for use on headers or emphasis
}
